//! SWIM membership via foca: node discovery, failure suspicion, and the live
//! member catalog.
//!
//! SWIM provides *hints only* — which nodes exist, their addresses, and
//! whether they look alive. Correctness (group membership, lock state, owner
//! liveness) is always decided by Raft; the membership controller consumes
//! this catalog merely to decide what to reconcile next.
//!
//! Wiring: one task owns the sans-io [`foca::Foca`] instance and socket reads,
//! driving it from received datagrams, scheduled timers, optional manual
//! gossip ticks, and seed-resolution updates. Socket writes and DNS resolution
//! are kept outside the Foca critical path so SWIM timers do not drift behind
//! unrelated I/O. Members are published to a `watch` channel as a map of
//! numeric node id → identity.

use std::collections::{BTreeMap, BinaryHeap};
use std::net::SocketAddr;
use std::num::{NonZeroU32, NonZeroUsize};
use std::sync::atomic::{AtomicU64, AtomicUsize, Ordering};
use std::sync::Arc;
use std::time::Duration;

use bytes::Bytes;
use foca::{BincodeCodec, Foca, NoCustomBroadcast, Timer};
use rand::rngs::StdRng;
use rand::SeedableRng;
use serde::{Deserialize, Serialize};
use tokio::net::UdpSocket;
use tokio::sync::{mpsc, watch};
use tracing::{debug, info, warn};

use crate::raft::types::NodeMeta;

/// SWIM identity: who a node is plus everything peers need to reach it.
/// `incarnation` makes re-joins win address conflicts against stale entries.
///
/// The identity's cluster-wide `Addr` is the **advertised** `ip:port` gossip
/// address (`meta.gossip_addr`): joining works by announcing to a seed's
/// resolved address, and foca only accepts announces whose destination `Addr`
/// matches the receiver's own — an address is the one thing both sides can
/// agree on before they have exchanged identities.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct NodeIdentity {
    pub node_id: u64,
    pub meta: NodeMeta,
    pub incarnation: u64,
}

impl foca::Identity for NodeIdentity {
    type Addr = String;

    fn addr(&self) -> String {
        self.meta.gossip_addr.clone()
    }

    fn win_addr_conflict(&self, adversary: &Self) -> bool {
        self.node_id == adversary.node_id && self.incarnation > adversary.incarnation
    }

    fn renew(&self) -> Option<Self> {
        Some(Self {
            node_id: self.node_id,
            meta: self.meta.clone(),
            incarnation: self.incarnation + 1,
        })
    }
}

/// Live cluster members as currently believed by SWIM (self included).
pub type MemberMap = BTreeMap<u64, NodeIdentity>;

#[derive(Clone)]
pub struct ClusterMembers {
    rx: watch::Receiver<MemberMap>,
    local: Arc<std::sync::RwLock<NodeIdentity>>,
    metrics: Arc<GossipMetrics>,
}

impl ClusterMembers {
    /// Subscribe to membership updates.
    pub fn watch(&self) -> watch::Receiver<MemberMap> {
        self.rx.clone()
    }

    /// Current snapshot of live members.
    pub fn snapshot(&self) -> MemberMap {
        self.rx.borrow().clone()
    }

    pub fn local(&self) -> NodeIdentity {
        self.local
            .read()
            .expect("local gossip identity lock poisoned")
            .clone()
    }

    pub fn metrics(&self) -> Arc<GossipMetrics> {
        self.metrics.clone()
    }
}

type FocaInstance =
    Foca<NodeIdentity, BincodeCodec<bincode::config::Configuration>, StdRng, NoCustomBroadcast>;

/// Runtime knobs for the SWIM layer. Defaults follow Foca's LAN profile and
/// keep only the seed-bootstrap cadence in pathlockd.
#[derive(Debug, Clone)]
pub struct GossipOptions {
    /// Expected active members; used by Foca to size dissemination and
    /// suspicion timing.
    pub cluster_size: NonZeroU32,
    /// Maximum UDP payload accepted/generated by Foca.
    pub max_packet_size: NonZeroUsize,
    /// How often DNS seeds are refreshed and announced while this node is
    /// lonely.
    pub seed_announce_interval: Duration,
    /// Optional extra `Foca::gossip()` cadence. `None` relies on Foca's
    /// built-in periodic gossip from the config.
    pub manual_gossip_interval: Option<Duration>,
    /// Whether to keep Foca's periodic announce/gossip timers enabled.
    pub foca_periodic: bool,
    /// Bounded UDP writer queue depth.
    pub send_queue_depth: usize,
}

impl Default for GossipOptions {
    fn default() -> Self {
        Self {
            cluster_size: NonZeroU32::new(32).expect("nonzero"),
            max_packet_size: NonZeroUsize::new(1400).expect("nonzero"),
            seed_announce_interval: Duration::from_secs(5),
            manual_gossip_interval: None,
            foca_periodic: true,
            send_queue_depth: 1024,
        }
    }
}

impl GossipOptions {
    fn foca_config(&self) -> foca::Config {
        let mut config = foca::Config::new_lan(self.cluster_size);
        config.max_packet_size = self.max_packet_size;
        if !self.foca_periodic {
            config.periodic_announce = None;
            config.periodic_announce_to_down_members = None;
            config.periodic_gossip = None;
        }
        config
    }
}

