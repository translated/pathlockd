#[derive(Clone)]
struct SeedTarget {
    seed: String,
    addr: SocketAddr,
    target: NodeIdentity,
}

struct OutboundDatagram {
    dest: SocketAddr,
    data: Bytes,
}

/// Start the SWIM layer: bind the UDP socket, join via `seed_nodes`
/// (`host:port` gossip addresses, DNS-resolvable), and publish the member
/// catalog.
pub async fn start_gossip(
    local: NodeIdentity,
    bind_addr: SocketAddr,
    seed_nodes: Vec<String>,
) -> anyhow::Result<ClusterMembers> {
    start_gossip_with_options(local, bind_addr, seed_nodes, GossipOptions::default()).await
}

pub async fn start_gossip_with_options(
    local: NodeIdentity,
    bind_addr: SocketAddr,
    seed_nodes: Vec<String>,
    options: GossipOptions,
) -> anyhow::Result<ClusterMembers> {
    anyhow::ensure!(
        options.send_queue_depth > 0,
        "gossip send_queue_depth must be > 0"
    );
    anyhow::ensure!(
        !options.seed_announce_interval.is_zero(),
        "gossip seed_announce_interval must be > 0"
    );
    if let Some(interval) = options.manual_gossip_interval {
        anyhow::ensure!(
            !interval.is_zero(),
            "gossip manual_gossip_interval must be > 0 when enabled"
        );
    }

    let socket = Arc::new(
        UdpSocket::bind(bind_addr)
            .await
            .map_err(|e| anyhow::anyhow!("binding gossip socket {bind_addr}: {e}"))?,
    );
    info!(%bind_addr, node_id = local.node_id, "gossip listening");

    let mut initial = MemberMap::new();
    initial.insert(local.node_id, local.clone());
    let (tx, rx) = watch::channel(initial);
    let local_state = Arc::new(std::sync::RwLock::new(local.clone()));
    let metrics = Arc::new(GossipMetrics::default());
    metrics.set_member_count(1);
    metrics.set_local_incarnation(local.incarnation);

    let config = options.foca_config();
    let max_packet_size = config.max_packet_size.get();
    let foca: FocaInstance = Foca::new(
        local.clone(),
        config,
        StdRng::from_os_rng(),
        BincodeCodec(bincode::config::standard()),
    );

    let (send_tx, send_rx) = mpsc::channel(options.send_queue_depth);
    tokio::spawn(socket_writer(socket.clone(), send_rx, metrics.clone()));

    let seed_rx = spawn_seed_resolver(
        seed_nodes,
        local.meta.gossip_addr.clone(),
        options.seed_announce_interval,
        metrics.clone(),
    );

    tokio::spawn(gossip_loop(
        foca,
        socket,
        tx,
        local_state.clone(),
        send_tx,
        seed_rx,
        max_packet_size,
        options.manual_gossip_interval,
        metrics.clone(),
    ));

    Ok(ClusterMembers {
        rx,
        local: local_state,
        metrics,
    })
}

/// Resolve a seed's gossip address. The seed's node id and metadata are
/// unknown until it answers, so the announce target is a placeholder identity
/// whose `Addr` (the resolved `ip:port`) matches the seed's advertised
/// address — that match is what makes the seed accept the announce; the real
/// identity arrives with its feed reply. Placeholder ids sit far outside the
/// ordinal range and are filtered from the published catalog.
async fn resolve_seed(seed: &str) -> Option<(SocketAddr, NodeIdentity)> {
    let mut addrs = tokio::net::lookup_host(seed).await.ok()?;
    let addr = addrs.next()?;
    Some((
        addr,
        NodeIdentity {
            node_id: u64::MAX ^ seed_hash(seed),
            meta: NodeMeta {
                name: format!("seed:{seed}"),
                raft_addr: String::new(),
                public_addr: String::new(),
                gossip_addr: addr.to_string(),
            },
            incarnation: 0,
        },
    ))
}

fn seed_hash(seed: &str) -> u64 {
    xxhash_rust::xxh3::xxh3_64(seed.as_bytes()) >> 16
}

/// The address this node advertises in its SWIM identity, raft membership,
/// and event fan-out: explicit override > specified bind IP > auto-detected
/// outbound IP (the UDP-connect trick — no packet is actually sent).
pub fn advertised_addr(
    bind: SocketAddr,
    advertise_override: Option<&str>,
    probe_target: Option<&SocketAddr>,
) -> anyhow::Result<SocketAddr> {
    if let Some(advertised) = advertise_override {
        return advertised
            .parse()
            .map_err(|e| anyhow::anyhow!("advertise addr {advertised}: {e}"));
    }
    if !bind.ip().is_unspecified() {
        return Ok(bind);
    }
    let probe = probe_target
        .copied()
        .unwrap_or_else(|| "8.8.8.8:53".parse().expect("static addr"));
    let sock = std::net::UdpSocket::bind(("0.0.0.0", 0))
        .map_err(|e| anyhow::anyhow!("probing local address: {e}"))?;
    sock.connect(probe)
        .map_err(|e| anyhow::anyhow!("probing local address via {probe}: {e}"))?;
    let local = sock
        .local_addr()
        .map_err(|e| anyhow::anyhow!("probing local address: {e}"))?;
    Ok(SocketAddr::new(local.ip(), bind.port()))
}

struct PendingTimer {
    fire_at: tokio::time::Instant,
    timer: Timer<NodeIdentity>,
    seq: u64,
}

impl PartialEq for PendingTimer {
    fn eq(&self, other: &Self) -> bool {
        self.fire_at == other.fire_at && self.seq == other.seq
    }
}
impl Eq for PendingTimer {}
impl PartialOrd for PendingTimer {
    fn partial_cmp(&self, other: &Self) -> Option<std::cmp::Ordering> {
        Some(self.cmp(other))
    }
}
impl Ord for PendingTimer {
    fn cmp(&self, other: &Self) -> std::cmp::Ordering {
        // Min-heap by fire time (BinaryHeap is a max-heap).
        other
            .fire_at
            .cmp(&self.fire_at)
            .then(other.seq.cmp(&self.seq))
    }
}

async fn gossip_loop(
    mut foca: FocaInstance,
    socket: Arc<UdpSocket>,
    tx: watch::Sender<MemberMap>,
    local: Arc<std::sync::RwLock<NodeIdentity>>,
    send_tx: mpsc::Sender<OutboundDatagram>,
    mut seed_rx: Option<mpsc::Receiver<Vec<SeedTarget>>>,
    max_packet_size: usize,
    manual_gossip_interval: Option<Duration>,
    metrics: Arc<GossipMetrics>,
) {
    let mut runtime = foca::AccumulatingRuntime::new();
    let mut timers: BinaryHeap<PendingTimer> = BinaryHeap::new();
    let mut timer_seq = 0u64;
    // Gossip-address book for datagram delivery (node id → socket addr).
    let mut addr_book: BTreeMap<u64, SocketAddr> = BTreeMap::new();
    let mut seed_targets: BTreeMap<String, SeedTarget> = BTreeMap::new();

    let mut recv_buf = vec![0u8; max_packet_size];
    let mut gossip_tick = manual_gossip_interval.map(|interval| {
        let mut tick = tokio::time::interval(interval);
        tick.set_missed_tick_behavior(tokio::time::MissedTickBehavior::Delay);
        tick
    });

    loop {
        let next_timer = timers.peek().map(|t| t.fire_at);
        tokio::select! {
            recv = socket.recv_from(&mut recv_buf) => {
                match recv {
                    Ok((len, _from)) => {
                        if let Err(e) = foca.handle_data(&recv_buf[..len], &mut runtime) {
                            metrics.bad_datagrams.fetch_add(1, Ordering::Relaxed);
                            debug!(error = %e, "gossip: bad datagram");
                        }
                    }
                    Err(e) => {
                        warn!(error = %e, "gossip: socket recv failed");
                        tokio::time::sleep(std::time::Duration::from_millis(100)).await;
                    }
                }
            }
            _ = async {
                match next_timer {
                    Some(at) => tokio::time::sleep_until(at).await,
                    None => std::future::pending().await,
                }
            } => {
                let now = tokio::time::Instant::now();
                while let Some(timer) = pop_due_timer(&mut timers, now) {
                    if let Err(e) = foca.handle_timer(timer, &mut runtime) {
                        debug!(error = %e, "gossip: timer handling failed");
                    }
                }
            }
            _ = tick_optional(&mut gossip_tick) => {
                if let Err(e) = foca.gossip(&mut runtime) {
                    debug!(error = %e, "gossip: round failed");
                }
            }
            targets = recv_seed_targets(&mut seed_rx) => {
                for target in targets {
                    addr_book.insert(target.target.node_id, target.addr);
                    seed_targets.insert(target.seed.clone(), target);
                }
                if foca.num_members() <= 1 {
                    for seed in seed_targets.values() {
                        if let Err(e) = foca.announce(seed.target.clone(), &mut runtime) {
                            debug!(seed = %seed.seed, error = %e, "gossip: announce failed");
                        }
                    }
                }
            }
        }

        // Drain the runtime: sends, new timers, notifications.
        while let Some((to, data)) = runtime.to_send() {
            let dest = resolve_member_addr(&to, &mut addr_book);
            if let Some(dest) = dest {
                enqueue_send(&send_tx, dest, data, &metrics);
            } else {
                metrics.unresolved_targets.fetch_add(1, Ordering::Relaxed);
                debug!(node = to.node_id, addr = %to.meta.gossip_addr, "gossip: unresolved send target");
            }
        }
        while let Some((after, timer)) = runtime.to_schedule() {
            timer_seq += 1;
            timers.push(PendingTimer {
                fire_at: tokio::time::Instant::now() + after,
                timer,
                seq: timer_seq,
            });
        }
        metrics.set_timer_backlog(timers.len());
        let mut membership_changed = false;
        while let Some(notification) = runtime.to_notify() {
            membership_changed |= handle_notification(notification, &local, &metrics);
        }
        if membership_changed {
            let mut members: MemberMap = foca
                .iter_members()
                .map(|m| (m.id().node_id, m.id().clone()))
                .filter(|(id, ident)| !is_placeholder(*id, ident))
                .collect();
            let local_identity = local
                .read()
                .expect("local gossip identity lock poisoned")
                .clone();
            members.insert(local_identity.node_id, local_identity);
            for member in members.values() {
                if let Ok(addr) = member.meta.gossip_addr.parse() {
                    addr_book.insert(member.node_id, addr);
                }
            }
            let count = members.len();
            if tx.send(members).is_err() {
                info!("gossip: no subscribers left; stopping");
                return;
            }
            metrics.set_member_count(count);
            debug!(members = count, "gossip: membership updated");
        }
    }
}

fn pop_due_timer(
    timers: &mut BinaryHeap<PendingTimer>,
    now: tokio::time::Instant,
) -> Option<Timer<NodeIdentity>> {
    if timers.peek().is_some_and(|pending| pending.fire_at <= now) {
        timers.pop().map(|pending| pending.timer)
    } else {
        None
    }
}

async fn tick_optional(tick: &mut Option<tokio::time::Interval>) {
    match tick {
        Some(tick) => {
            tick.tick().await;
        }
        None => std::future::pending().await,
    }
}

async fn recv_seed_targets(rx: &mut Option<mpsc::Receiver<Vec<SeedTarget>>>) -> Vec<SeedTarget> {
    let Some(receiver) = rx.as_mut() else {
        return std::future::pending().await;
    };
    match receiver.recv().await {
        Some(targets) => targets,
        None => {
            *rx = None;
            Vec::new()
        }
    }
}

fn handle_notification(
    notification: foca::OwnedNotification<NodeIdentity>,
    local: &Arc<std::sync::RwLock<NodeIdentity>>,
    metrics: &GossipMetrics,
) -> bool {
    use foca::OwnedNotification;
    match notification {
        OwnedNotification::MemberUp(id) => {
            metrics.member_up.fetch_add(1, Ordering::Relaxed);
            info!(node = id.node_id, name = %id.meta.name, "gossip: member up");
            true
        }
        OwnedNotification::MemberDown(id) => {
            metrics.member_down.fetch_add(1, Ordering::Relaxed);
            info!(node = id.node_id, name = %id.meta.name, "gossip: member down");
            true
        }
        OwnedNotification::Rename(old, new) => {
            metrics.renamed.fetch_add(1, Ordering::Relaxed);
            if old.node_id != new.node_id {
                warn!(
                    old_node = old.node_id,
                    new_node = new.node_id,
                    gossip_addr = %new.meta.gossip_addr,
                    "gossip: identity changed node id for the same address"
                );
            } else {
                debug!(
                    node = new.node_id,
                    old_inc = old.incarnation,
                    new_inc = new.incarnation,
                    "gossip: member renamed"
                );
            }
            true
        }
        OwnedNotification::Rejoin(new_identity) => {
            metrics.rejoins.fetch_add(1, Ordering::Relaxed);
            metrics.set_local_incarnation(new_identity.incarnation);
            *local.write().expect("local gossip identity lock poisoned") = new_identity.clone();
            info!(
                node = new_identity.node_id,
                incarnation = new_identity.incarnation,
                "gossip: rejoined with renewed identity"
            );
            true
        }
        OwnedNotification::Idle => {
            metrics.idle.fetch_add(1, Ordering::Relaxed);
            debug!("gossip: cluster idle");
            true
        }
        OwnedNotification::Active => {
            debug!("gossip: cluster active");
            false
        }
        OwnedNotification::Defunct => {
            warn!("gossip: local identity declared defunct");
            false
        }
    }
}

fn spawn_seed_resolver(
    seed_nodes: Vec<String>,
    local_gossip_addr: String,
    interval: Duration,
    metrics: Arc<GossipMetrics>,
) -> Option<mpsc::Receiver<Vec<SeedTarget>>> {
    if seed_nodes.is_empty() {
        return None;
    }
    let (tx, rx) = mpsc::channel(1);
    tokio::spawn(async move {
        let mut tick = tokio::time::interval(interval);
        tick.set_missed_tick_behavior(tokio::time::MissedTickBehavior::Delay);
        loop {
            tick.tick().await;
            let mut targets = Vec::new();
            for seed in &seed_nodes {
                match resolve_seed(seed).await {
                    Some((addr, target)) if addr.to_string() != local_gossip_addr => {
                        targets.push(SeedTarget {
                            seed: seed.clone(),
                            addr,
                            target,
                        });
                    }
                    Some(_) => {}
                    None => {
                        metrics
                            .seed_resolve_failures
                            .fetch_add(1, Ordering::Relaxed);
                        debug!(%seed, "gossip: seed resolve failed");
                    }
                }
            }
            if tx.send(targets).await.is_err() {
                return;
            }
        }
    });
    Some(rx)
}

async fn socket_writer(
    socket: Arc<UdpSocket>,
    mut rx: mpsc::Receiver<OutboundDatagram>,
    metrics: Arc<GossipMetrics>,
) {
    while let Some(datagram) = rx.recv().await {
        metrics.send_queue_depth.fetch_sub(1, Ordering::Relaxed);
        if let Err(e) = socket.send_to(&datagram.data, datagram.dest).await {
            metrics.send_failures.fetch_add(1, Ordering::Relaxed);
            debug!(to = %datagram.dest, error = %e, "gossip: send failed");
        }
    }
}

fn enqueue_send(
    send_tx: &mpsc::Sender<OutboundDatagram>,
    dest: SocketAddr,
    data: Bytes,
    metrics: &GossipMetrics,
) {
    metrics.send_queue_depth.fetch_add(1, Ordering::Relaxed);
    match send_tx.try_send(OutboundDatagram { dest, data }) {
        Ok(()) => {}
        Err(tokio::sync::mpsc::error::TrySendError::Full(_)) => {
            metrics.send_queue_depth.fetch_sub(1, Ordering::Relaxed);
            metrics.send_dropped.fetch_add(1, Ordering::Relaxed);
            debug!(to = %dest, "gossip: dropping outbound datagram; send queue full");
        }
        Err(tokio::sync::mpsc::error::TrySendError::Closed(_)) => {
            metrics.send_queue_depth.fetch_sub(1, Ordering::Relaxed);
            metrics.send_dropped.fetch_add(1, Ordering::Relaxed);
            debug!(to = %dest, "gossip: dropping outbound datagram; writer closed");
        }
    }
}

fn is_placeholder(id: u64, ident: &NodeIdentity) -> bool {
    // Seed placeholders carry no raft address and ids far outside the
    // ordinal range.
    id > u32::MAX as u64 && ident.meta.raft_addr.is_empty()
}

fn resolve_member_addr(
    to: &NodeIdentity,
    addr_book: &mut BTreeMap<u64, SocketAddr>,
) -> Option<SocketAddr> {
    if let Ok(addr) = to.meta.gossip_addr.parse() {
        addr_book.insert(to.node_id, addr);
        return Some(addr);
    }
    // Placeholder identities (seeds) resolve via DNS once and are cached.
    if let Some(addr) = addr_book.get(&to.node_id) {
        return Some(*addr);
    }
    None
}

