    #[test]
    fn due_timers_are_drained_in_deadline_order() {
        let now = tokio::time::Instant::now();
        let later = now + Duration::from_secs(1);
        let mut timers = BinaryHeap::new();
        let first = Timer::RemoveDown(identity(1, 0));
        let second = Timer::RemoveDown(identity(2, 0));
        let future = Timer::RemoveDown(identity(3, 0));

        timers.push(PendingTimer {
            fire_at: now,
            timer: second.clone(),
            seq: 2,
        });
        timers.push(PendingTimer {
            fire_at: now,
            timer: first.clone(),
            seq: 1,
        });
        timers.push(PendingTimer {
            fire_at: later,
            timer: future,
            seq: 3,
        });

        let mut popped = Vec::new();
        while let Some(timer) = pop_due_timer(&mut timers, now) {
            popped.push(timer);
        }

        assert_eq!(popped, vec![first, second]);
        assert_eq!(timers.len(), 1);
    }

    #[tokio::test]
    async fn two_nodes_discover_each_other() {
        let addr_a = free_local_addr().await;
        let addr_b = free_local_addr().await;
        let unused_seed = free_local_addr().await;
        let mut id_a = identity(1, 0);
        id_a.meta.gossip_addr = addr_a.to_string();
        let mut id_b = identity(2, 0);
        id_b.meta.gossip_addr = addr_b.to_string();

        let members_a = start_gossip_with_options(id_a, addr_a, vec![], test_options())
            .await
            .unwrap();
        let members_b = start_gossip_with_options(
            id_b,
            addr_b,
            vec![unused_seed.to_string(), addr_a.to_string()],
            test_options(),
        )
        .await
        .unwrap();

        let mut rx = members_b.watch();
        let found = tokio::time::timeout(Duration::from_secs(5), async {
            loop {
                if rx.borrow().len() >= 2 {
                    return true;
                }
                if rx.changed().await.is_err() {
                    return false;
                }
            }
        })
        .await
        .unwrap_or(false);
        assert!(found, "node B must discover node A via the seed announce");
        // A also learns about B.
        let mut rx_a = members_a.watch();
        let found_a = tokio::time::timeout(Duration::from_secs(5), async {
            loop {
                if rx_a.borrow().len() >= 2 {
                    return true;
                }
                if rx_a.changed().await.is_err() {
                    return false;
                }
            }
        })
        .await
        .unwrap_or(false);
        assert!(found_a, "node A must learn about node B");
    }

